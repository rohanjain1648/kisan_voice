# ── Public functions ──────────────────────────────────────────────────────────

def topic_list() -> list[tuple[str, str]]:
    """Return (key, display_label) for all topics."""
    return [(k, f"{v['icon']} {v['title']}") for k, v in TOPICS.items()]


def topic_labels() -> list[str]:
    return [f"{v['icon']} {v['title']}" for v in TOPICS.values()]


def topic_keys() -> list[str]:
    return list(TOPICS.keys())


def get_topic(key: str) -> dict | None:
    return TOPICS.get(key)


def get_topic_content(key: str) -> str:
    topic = TOPICS.get(key)
    if not topic:
        return "Topic not found."
    return topic["content"]


def get_topic_by_label(label: str) -> dict | None:
    for v in TOPICS.values():
        if label.strip().endswith(v["title"]) or v["title"] in label:
            return v
    return None


def get_context_for_llm(topic_key: str) -> str:
    """Compact context for LLM prompt."""
    topic = TOPICS.get(topic_key)
    if not topic:
        return ""
    # Strip markdown tables to save tokens; keep first 800 chars of content
    import re
    text = re.sub(r"\|[^\n]+\|", "", topic["content"])
    text = re.sub(r"-{3,}", "", text)
    text = re.sub(r"#{1,3}\s*", "", text)
    text = re.sub(r"\n{3,}", "\n", text).strip()
    return f"[Topic: {topic['title']}]\n{text[:800]}"


def search_topics(query: str) -> list[str]:
    """Return topic keys whose title/summary match the query keywords."""
    q = query.lower()
    return [
        k for k, v in TOPICS.items()
        if any(word in v["title"].lower() or word in v["summary"].lower()
               for word in q.split() if len(word) > 3)
    ]
